    private void Buscar() {
        if (this.TBuscar.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "El id es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.TBuscar.requestFocus();
            return;
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        this.transaccion = new LTransaccion().Consultar(Integer.parseInt(this.TBuscar.getText().trim()));
        if (this.transaccion != null && this.transaccion.getIdTransaccion() > 0) {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            

            this.txtCantidad.setText(String.valueOf(this.transaccion.getCantidad()));
            this.txtCantidad.setEditable(true);
            this.txtPrecioTotal.setText(String.valueOf(this.transaccion.getPrecioTotal()));
            this.txtPrecioTotal.setEditable(true);
            this.txtIdFactura.setText(String.valueOf(this.transaccion.getIdFactura()));
            this.txtIdFactura.setEditable(true);
            this.txtIdProducto.setText(String.valueOf(this.transaccion.getIdProducto()));
            this.txtIdProducto.setEditable(true);

            
            this.btnActualizar.setEnabled(true);
        } else {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(this, "No se encontr贸 la Transaccion con ese id.", "Aviso", JOptionPane.ERROR_MESSAGE);
            this.TBuscar.requestFocus();
        }
    }
    
    private void LimpiarFormulario() {
        this.txtCantidad.setText("");
        this.txtCantidad.setEditable(false);
        this.txtPrecioTotal.setText("");
        this.txtPrecioTotal.setEditable(false);
        this.txtIdFactura.setText("");
        this.txtIdFactura.setEditable(false);
        this.txtIdProducto.setText("");
        this.txtIdProducto.setEditable(false);
        this.TBuscar.requestFocus();
        this.transaccion = null;
    }


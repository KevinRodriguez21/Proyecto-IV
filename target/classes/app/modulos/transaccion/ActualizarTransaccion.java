    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (this.txtCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La cantidad es requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCantidad.requestFocus();
            return;
        }

        if (this.txtPrecioTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El APrecio Total es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtPrecioTotal.requestFocus();
            return;
        }

        if (this.txtIdFactura.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id de Factura es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtIdFactura.requestFocus();
            return;
        }

        if (this.txtIdProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id de Producto es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtIdProducto.requestFocus();
            return;
        }


        int operacion = new LTransaccion().Actualizar(new Transaccion(
            Integer.parseInt(this.TBuscar.getText().trim()),
            Integer.parseInt(this.txtCantidad.getText().trim()),
            Integer.parseInt(this.txtPrecioTotal.getText().trim()),
            Integer.parseInt(this.txtIdFactura.getText().trim()),
            Integer.parseInt(this.txtIdProducto.getText().trim())
        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet贸 la actualizaci贸n correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
            this.TBuscar.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar la actualizaci贸n del producto,"
                + " intente nuevamente, verifique que exista el profesor ingresado.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed


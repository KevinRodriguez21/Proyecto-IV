    private void CargarTabla() {
        ArrayList<Transaccion> transacciones = new LTransaccion().Listar();
        
        transacciones.sort(Comparator.comparingInt(Transaccion::getIdTransaccion));
        
        String[] columnas = new String[]{
            "Id",
            "Cantidad",
            "PrecioTotal",
            "idFactura",
            "idProducto"
        };
        
        DefaultTableModel obModelo = new DefaultTableModel(columnas, 0);
        
        for (Transaccion transaccion : transacciones) {
            obModelo.addRow(new Object[]{
                transaccion.getIdTransaccion()+ "",
                transaccion.getCantidad() + " ",
                transaccion.getPrecioTotal(),
                transaccion.getIdFactura(),
                transaccion.getIdProducto(),
            });
        }
        
        this.TTransaccion.setModel(obModelo);
    }
    
    private void CargarTabla(ArrayList<Transaccion> transacciones) {
        String[] columnas = new String[]{
            "Id",
            "Cantidad",
            "PrecioTotal",
            "idFactura",
            "idProducto"
        };
        
        DefaultTableModel obModelo = new DefaultTableModel(columnas, 0);
        
        for (Transaccion transaccion : transacciones) {
            obModelo.addRow(new Object[]{
                transaccion.getIdTransaccion()+ "",
                transaccion.getCantidad() + " ",
                transaccion.getPrecioTotal(),
                transaccion.getIdFactura(),
                transaccion.getIdProducto(),
            });
        }
        
        this.TTransaccion.setModel(obModelo);
    }
    
    
    private void Buscar() {
        String valor = this.txtBuscar.getText().trim().toLowerCase();

        if (!valor.isEmpty() && !valor.isBlank()) {
            ArrayList<Transaccion> clientes = new LTransaccion().Listar();

            if (clientes != null && !clientes.isEmpty()) {
                List<Transaccion> finales = clientes.stream()
                        .filter(emp -> (emp.getIdTransaccion() + "").equals(valor))
                        .collect(Collectors.toList());

                this.CargarTabla((ArrayList<Transaccion>) finales);
            }
        }
    }
    

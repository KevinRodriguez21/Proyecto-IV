    private void Buscar() {
        if (this.CBuscar.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "El id es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.CBuscar.requestFocus();
            return;
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        this.cliente = new LCliente().Consultar(Integer.parseInt(this.CBuscar.getText().trim()));
        if (this.cliente != null && this.cliente.getIdCliente() > 0) {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            

            this.txtNombre.setText(this.cliente.getNombreCliente());
            this.txtNombre.setEditable(true);
            this.txtApellido.setText(this.cliente.getApellidoCliente());
            this.txtApellido.setEditable(true);
            this.txtDireccion.setText(this.cliente.getDireccionCliente());
            this.txtDireccion.setEditable(true);
            this.txtTelefono.setText(String.valueOf(this.cliente.getTelefonoCliente()));
            this.txtTelefono.setEditable(true);
            this.txtCorreo.setText(this.cliente.getCorreoCliente());
            this.txtCorreo.setEditable(true);
            
            
            this.btnActualizar.setEnabled(true);
        } else {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(this, "No se encontr贸 el cliente con ese id.", "Aviso", JOptionPane.ERROR_MESSAGE);
            this.CBuscar.requestFocus();
        }
    }
    
    private void LimpiarFormulario() {
        this.txtNombre.setText("");
        this.txtNombre.setEditable(false);
        this.txtApellido.setText("");
        this.txtApellido.setEditable(false);
        this.txtDireccion.setText("");
        this.txtDireccion.setEditable(false);
        this.txtTelefono.setText("");
        this.txtTelefono.setEditable(false);
        this.txtCorreo.setText("");
        this.txtCorreo.setEditable(false);
        this.CBuscar.requestFocus();
        this.cliente = null;
    }


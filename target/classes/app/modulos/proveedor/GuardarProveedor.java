    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (this.PNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Nombre es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.PNombre.requestFocus();
            return;
        }

        if (this.PDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Direccion es Requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.PDireccion.requestFocus();
            return;
        }

        if (this.PCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El correo es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.PCorreo.requestFocus();
            return;
        }

        

        int operacion = new LProveedor().Guardar(new Proveedor(
                0,
                this.PNombre.getText().trim(),
                this.PDireccion.getText().trim(),
                this.PCorreo.getText().trim()
                
        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet√≥ el registro correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar el registro del proveedor,"
                    + " intente nuevamente, verifique que no exista el producto ingresado.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


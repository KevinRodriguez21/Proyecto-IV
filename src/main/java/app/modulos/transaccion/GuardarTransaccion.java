    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (this.TCantidad.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Cantidad es requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.TCantidad.requestFocus();
            return;
        }

        if (this.TPrecioTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Precio Total es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.TPrecioTotal.requestFocus();
            return;
        }

        if (this.TIdFactura.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El ID de Factura es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.TIdFactura.requestFocus();
            return;
        }

        if (this.TIdProducto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id de Inventario es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.TIdProducto.requestFocus();
            return;
        }

        int operacion = new LTransaccion().Guardar(new Transaccion(
            0,
            Integer.parseInt(this.TCantidad.getText().trim()),
            Integer.parseInt(this.TPrecioTotal.getText().trim()),
            Integer.parseInt(this.TIdFactura.getText().trim()),
            Integer.parseInt(this.TIdProducto.getText().trim())
        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet√≥ el registro correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar el registro de la transaccion,"
                + " intente nuevamente, verifique que no exista la transaccion ingresada.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void LimpiarFormulario() {
        this.TCantidad.setText("");
        this.TPrecioTotal.setText("");
        this.TIdFactura.setText("");
        this.TIdProducto.setText("");
    }
    

    private void Buscar() {
        if (this.UBuscar.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "El id es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.UBuscar.requestFocus();
            return;
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        this.usuario = new LUsuario().Consultar(Integer.parseInt(this.UBuscar.getText().trim()));
        if (this.usuario != null && this.usuario.getIdUsuario() > 0) {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            

            this.txtNombre.setText(this.usuario.getNombreUsuario());
            this.txtNombre.setEditable(true);
            this.txtApellido.setText(this.usuario.getApellidoUsuario());
            this.txtApellido.setEditable(true);
            this.txtPuesto.setText(this.usuario.getPuesto());
            this.txtPuesto.setEditable(true);
            this.txtTelefono.setText(String.valueOf(this.usuario.getTelefonoUsuario()));
            this.txtTelefono.setEditable(true);
            this.txtCorreo.setText(this.usuario.getCorreoUsuario());
            this.txtCorreo.setEditable(true);
            
            
            this.btnActualizar.setEnabled(true);
        } else {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(this, "No se encontr贸 el usuario con ese id.", "Aviso", JOptionPane.ERROR_MESSAGE);
            this.UBuscar.requestFocus();
        }
    }
    
    private void LimpiarFormulario() {
        this.txtNombre.setText("");
        this.txtNombre.setEditable(false);
        this.txtApellido.setText("");
        this.txtApellido.setEditable(false);
        this.txtPuesto.setText("");
        this.txtPuesto.setEditable(false);
        this.txtTelefono.setText("");
        this.txtTelefono.setEditable(false);
        this.txtCorreo.setText("");
        this.txtCorreo.setEditable(false);
        this.UBuscar.requestFocus();
        this.usuario = null;
    }


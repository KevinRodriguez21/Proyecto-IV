    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (this.txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El nombre es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtNombre.requestFocus();
            return;
        }

        if (this.txtApellido.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Apellido es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtApellido.requestFocus();
            return;
        }

        if (this.txtPuesto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Puesto es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtPuesto.requestFocus();
            return;
        }

        if (this.txtTelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Telefono es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtTelefono.requestFocus();
            return;
        }

        if (this.txtCorreo.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Correo es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtCorreo.requestFocus();
            return;
        }

        int operacion = new LUsuario().Actualizar(new Usuario(
            Integer.parseInt(this.UBuscar.getText().trim()),
            this.txtNombre.getText().trim(),
            this.txtApellido.getText().trim(),
            this.txtPuesto.getText().trim(),
            Integer.parseInt(this.txtTelefono.getText().trim()),
            this.txtCorreo.getText().trim()

        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet贸 la actualizaci贸n correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
            this.UBuscar.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar la actualizaci贸n del Usuario,"
                + " intente nuevamente, verifique que exista el profesor ingresado.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed


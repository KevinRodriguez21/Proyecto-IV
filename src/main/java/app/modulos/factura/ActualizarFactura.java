    private void Buscar() {
        if (this.FBuscar.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "El id es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FBuscar.requestFocus();
            return;
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        this.factura = new LFactura().Consultar(Integer.parseInt(this.FBuscar.getText().trim()));
        if (this.factura != null && this.factura.getIdFactura() > 0) {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            

            this.txtFecha.setText(String.valueOf(this.factura.getFechaFactura()));
            this.txtFecha.setEditable(true);
            this.txtTotal.setText(String.valueOf(this.factura.getTotalFactura()));
            this.txtTotal.setEditable(true);
            this.txtGarantia.setText(String.valueOf(this.factura.getGarantia()));
            this.txtGarantia.setEditable(true);
            this.txtIdCliente.setText(String.valueOf(this.factura.getIdCliente()));
            this.txtIdCliente.setEditable(true);
            this.txtIdUsuario.setText(String.valueOf(this.factura.getIdUsuario()));
            this.txtIdUsuario.setEditable(true);
            
            
            this.btnActualizar.setEnabled(true);
        } else {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(this, "No se encontr贸 la Factura con ese id.", "Aviso", JOptionPane.ERROR_MESSAGE);
            this.FBuscar.requestFocus();
        }
    }
    
    private void LimpiarFormulario() {
        this.txtFecha.setText("");
        this.txtFecha.setEditable(false);
        this.txtTotal.setText("");
        this.txtTotal.setEditable(false);
        this.txtGarantia.setText("");
        this.txtGarantia.setEditable(false);
        this.txtIdCliente.setText("");
        this.txtIdCliente.setEditable(false);
        this.txtIdUsuario.setText("");
        this.txtIdUsuario.setEditable(false);
        this.FBuscar.requestFocus();
        this.factura = null;
    }


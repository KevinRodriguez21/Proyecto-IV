    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (this.txtFecha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La fecha de la factura es requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtFecha.requestFocus();
            return;
        }

        if (this.txtTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Total de la Factura es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtTotal.requestFocus();
            return;
        }

        if (this.txtGarantia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Garantia es Requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtGarantia.requestFocus();
            return;
        }

        if (this.txtIdCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id de Cliente es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtIdCliente.requestFocus();
            return;
        }

        if (this.txtIdUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id de Usuario es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtIdUsuario.requestFocus();
            return;
        }

        String fechaTexto = this.txtFecha.getText().trim();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd"); 
        Date fecha;
        try {
            fecha = formatoFecha.parse(fechaTexto);
        } catch (ParseException e) {
            
            fecha = null; 
        }
        
        int operacion = new LFactura().Actualizar(new Factura(
            Integer.parseInt(this.FBuscar.getText().trim()),
            fecha,
            Integer.parseInt(this.txtTotal.getText().trim()),
            this.txtGarantia.getText().trim(),
            Integer.parseInt(this.txtIdCliente.getText().trim()),
            Integer.parseInt(this.txtIdUsuario.getText().trim())

        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet贸 la actualizaci贸n correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
            this.FBuscar.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar la actualizaci贸n del producto,"
                + " intente nuevamente, verifique que exista el profesor ingresado.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed


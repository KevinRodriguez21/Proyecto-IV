    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (this.FFecha.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Fecha es Requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FFecha.requestFocus();
            return;
        }

        if (this.FTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Total es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FTotal.requestFocus();
            return;
        }

        if (this.FGarantia.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Garantia es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FGarantia.requestFocus();
            return;
        }

        if (this.FIdCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id Cliente es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FIdCliente.requestFocus();
            return;
        }
        if (this.FIdUsuario.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Id Cliente es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.FIdUsuario.requestFocus();
            return;
        }

        String fechaTexto = this.FFecha.getText().trim();
        SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd"); 
        Date fecha;
        try {
            fecha = formatoFecha.parse(fechaTexto);
        } catch (ParseException e) {
            
            fecha = null; 
        }
        
        int operacion = new LFactura().Guardar(new Factura(
            0,
            fecha,
            Integer.parseInt(this.FTotal.getText().trim()),
            this.FGarantia.getText().trim(),
            Integer.parseInt(this.FIdCliente.getText().trim()),
            Integer.parseInt(this.FIdUsuario.getText().trim())
        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se complet√≥ el registro correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar el registro de la Factura,"
                + " intente nuevamente, verifique que no exista la factura ingresada.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


    private void Buscar() {
        if (this.IBuscar.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "La identificación es requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.IBuscar.requestFocus();
            return;
        }
        
        this.setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        this.inventario = new LInventario().Consultar(Integer.parseInt(this.IBuscar.getText().trim()));
        if (this.inventario != null && this.inventario.getIdProducto() > 0) {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            

            this.txtNombre.setText(this.inventario.getNombreProducto());
            this.txtNombre.setEditable(true);
            this.txtDescripcion.setText(this.inventario.getDescripcion());
            this.txtDescripcion.setEditable(true);
            this.txtPrecioVenta.setText(String.valueOf(this.inventario.getPrecioVenta()));
            this.txtPrecioVenta.setEditable(true);
            this.txtStock.setText(String.valueOf(this.inventario.getStock()));
            this.txtStock.setEditable(true);
            this.txtIdProveedor.setText(String.valueOf(this.inventario.getIdProveedor()));
            this.txtIdProveedor.setEditable(true);
            
            
            this.btnActualizar.setEnabled(true);
        } else {
            this.setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            JOptionPane.showMessageDialog(this, "No se encontró el producto con ese id.", "Aviso", JOptionPane.ERROR_MESSAGE);
            this.IBuscar.requestFocus();
        }
    }
    
    private void LimpiarFormulario() {
        this.txtNombre.setText("");
        this.txtNombre.setEditable(false);
        this.txtDescripcion.setText("");
        this.txtDescripcion.setEditable(false);
        this.txtPrecioVenta.setText("");
        this.txtPrecioVenta.setEditable(false);
        this.txtStock.setText("");
        this.txtStock.setEditable(false);
        this.txtIdProveedor.setText("");
        this.txtIdProveedor.setEditable(false);
        this.IBuscar.requestFocus();
        this.inventario = null;
    }


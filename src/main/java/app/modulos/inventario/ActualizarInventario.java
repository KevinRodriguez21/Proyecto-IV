    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (this.txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El nombre es requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtNombre.requestFocus();
            return;
        }

        if (this.txtDescripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "La Descripcion es Requerida.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtDescripcion.requestFocus();
            return;
        }

        if (this.txtPrecioVenta.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Precio de Venta es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtPrecioVenta.requestFocus();
            return;
        }

        if (this.txtStock.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El Stock es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtStock.requestFocus();
            return;
        }
        if (this.txtIdProveedor.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "El id del proveedor es Requerido.", "Aviso", JOptionPane.WARNING_MESSAGE);
            this.txtIdProveedor.requestFocus();
            return;
        }

        int operacion = new LInventario().Actualizar(new Inventario(
                Integer.parseInt(this.IBuscar.getText().trim()),
            this.txtNombre.getText().trim(),
            this.txtDescripcion.getText().trim(),
            Integer.parseInt(this.txtPrecioVenta.getText().trim()),
            Integer.parseInt(this.txtStock.getText().trim()),
            Integer.parseInt(this.txtIdProveedor.getText().trim())
        ));

        if (operacion > 0) {
            JOptionPane.showMessageDialog(this, "Se completó la actualización correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            this.LimpiarFormulario();
            this.IBuscar.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo completar la actualización del producto,"
                + " intente nuevamente, verifique que exista el profesor ingresado.", "Aviso", JOptionPane.ERROR_MESSAGE);
        }                                        

    }//GEN-LAST:event_btnActualizarActionPerformed

